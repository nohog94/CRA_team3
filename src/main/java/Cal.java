public class Cal {
    public int getGop(int a, int b) {
        return a * b;
    }
    public float getDivide(int a, int b) {
        if (b == 0) {
            return 0;
        }
        return a/b;
    }

    public int minus(int a, int b){
        return a-b;
    }

    int getSumSum(int a, int b, int c) {
        return a+b+c;
    }

    int getSum(int a, int b) {
        return a+b;
    }
}
